# 1'. Вычислить число Пи c заданной точностью d
import math

d = float(input("Введите точность: "))
rate = 0
while (d * 10 ** rate) % 1 > 0: rate += 1
print(f'При d = {d}, π  = {round(math.pi, rate)}')

